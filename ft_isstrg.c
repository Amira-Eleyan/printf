#include "ft_printf.h"

int	ft_isstrg(char *s)
{
	int	i;

	i = 0;
	while (s && *s)
	{
		ft_character(*s);
		i++;
		s++;
	}
	if (s == NULL)
	{
		i += write(1, "(null)", 6);
	}
	return (i);
}
